    public boolean etiquetas() {
        boolean bandera = true;
        
        if (this.combo_Cliente.getText().isEmpty()) {
            this.etiqueta_Indicador_Cliente.setVisible(true);
            bandera = false;
        } else {
            this.etiqueta_Indicador_Cliente.setVisible(false);
        }

        if (this.campo_Ciudad.getSelectedIndex() == - 1) {
            this.etiqueta_Indicador_Ciudad.setVisible(true);
            bandera = false;
        } else {
            this.etiqueta_Indicador_Ciudad.setVisible(false);
        }

        if( this.campo_RUC.getText().matches( "[0-9][0-9]{12}" ) && this.radio_Boton_RUC.isSelected() ){
            this.etiqueta_Indicador_RUC.setVisible( false );
        }else if(this.campo_RUC.getText().matches( "[0-9][0-9]{9}" ) && this.radio_Boton_CI.isSelected()){
            this.etiqueta_Indicador_RUC.setVisible( false );
        }
        else{
            this.etiqueta_Indicador_RUC.setVisible( true );
            bandera = false;
        }

        if (this.campo_Direccion.getText().isEmpty()) {
            this.etiqueta_Indicador_Direccion.setVisible(true);
            bandera = false;
        } else {
            this.etiqueta_Indicador_Direccion.setVisible(false);
        }	

        if (this.campo_Telefono.getText().matches("[0][1-9][0-9]{7}")) {
            this.etiqueta_Indicador_Telefono.setVisible(false);
        } else {
            this.etiqueta_Indicador_Telefono.setVisible(true);
            bandera = false;
        }

        if (this.campo_Celular.getText().matches("[0][8-9][0-9]{8}")) {
            this.etiqueta_Indicador_Celular.setVisible(false);
        } else {
            this.etiqueta_Indicador_Celular.setVisible(true);
            bandera = false;
        }

        if (this.campo_Correo.getText().matches("[a-zA-Z_\\d]+@[a-zA-Z]+(\\.[a-zA-Z]*)+")) {
            this.etiqueta_Indicador_Correo.setVisible(false);
        } else {
            this.etiqueta_Indicador_Correo.setVisible(true);
            bandera = false;
        }

        if (this.campo_Contacto.getText().isEmpty()) {
            this.etiqueta_Indicador_Contacto.setVisible(true);
            bandera = false;
        } else {
            this.etiqueta_Indicador_Contacto.setVisible(false);
        }
        return bandera;
    }

